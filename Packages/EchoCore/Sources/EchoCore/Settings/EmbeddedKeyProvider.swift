    // MARK: - Mutable State

    /// Tracks whether seeding has already been attempted this process lifetime.
    nonisolated(unsafe) private static var didSeed = false
    private static let seedLock = NSLock()

    // MARK: - Init

    private init() {}

    // MARK: - Public API

    /// Decrypt embedded keys and write any missing ones to the Keychain.
    ///
    /// Safe to call multiple times; only the first invocation performs work.
    /// Never overwrites user-provided keys.
    ///
    /// - Parameter keyStore: The `SecureKeyStore` instance to write into.
    public func seedKeychainIfNeeded(keyStore: SecureKeyStore = SecureKeyStore()) {
        Self.seedLock.lock()
        let alreadySeeded = Self.didSeed
        if !alreadySeeded {
            Self.didSeed = true
        }
        Self.seedLock.unlock()

        guard !alreadySeeded else { return }

        guard !Self.encryptedPayload.isEmpty else { return }

        guard let decryptedKeys = decryptPayload() else { return }

        for keyId in Self.keyIds {
            guard let value = decryptedKeys[keyId], !value.isEmpty else {
                continue
            }

            if keyStore.hasKey(for: keyId) {
                continue
            }

            do {
                try keyStore.store(key: value, for: keyId)
            } catch {
                // Silently skip â€” the user can still enter keys manually.
                // Logging is intentionally omitted to avoid leaking key metadata.
            }
        }
    }

    // MARK: - Decryption

    private func decryptPayload() -> [String: String]? {
        let payload = Data(Self.encryptedPayload)

        guard payload.count > 28 else {
            // Minimum: 12 (nonce) + 0 (ciphertext) + 16 (tag) = 28
            return nil
        }

        let derivedKey = Self.deriveKey()

        do {
            let sealedBox = try AES.GCM.SealedBox(combined: payload)
            let plaintext = try AES.GCM.open(sealedBox, using: derivedKey)
            let decoded = try JSONSerialization.jsonObject(with: plaintext) as? [String: String]
            return decoded
        } catch {
            return nil
        }
    }

    private static func deriveKey() -> SymmetricKey {
        let inputKey = SymmetricKey(data: Data(masterSecret.utf8))
        let salt = Data(hkdfSalt.utf8)
        let info = Data(hkdfInfo.utf8)

        return HKDF<SHA256>.deriveKey(
            inputKeyMaterial: inputKey,
            salt: salt,
            info: info,
            outputByteCount: 32
        )
    }
